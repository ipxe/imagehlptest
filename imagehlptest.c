static BOOL check_map_and_load ( const char *filename ) {
	LOADED_IMAGE image;

	/* Test ability to MapAndLoad() */
	if ( ! MapAndLoad ( filename, NULL, &image, FALSE, TRUE ) ) {
		fprintf ( stderr, "%s: failed MapAndLoad() with error %#x\n",
			  filename, GetLastError() );
		return FALSE;
	}

	/* Free resources */
	UnMapAndLoad ( &image );

	return TRUE;
}

static BOOL check_certificates ( const char *filename ) {
	HANDLE fh;
	WIN_CERTIFICATE *cert;
	DWORD num_certs;
	DWORD index;

	/* Create test certificate */
	cert = malloc ( sizeof ( *cert ) + sizeof ( test_cert_data ) );
	if ( ! cert ) {
		fprintf ( stderr, "%s: out of memory\n", filename );
		goto err_alloc;
	}
	cert->dwLength = sizeof ( test_cert_data );
	cert->wRevision = WIN_CERT_REVISION_1_0;
	cert->wCertificateType = WIN_CERT_TYPE_PKCS_SIGNED_DATA;
	memcpy ( cert->bCertificate, test_cert_data,
		 sizeof ( test_cert_data ) );

	/* Open file */
	fh = CreateFile ( filename, ( GENERIC_READ | GENERIC_WRITE ),
			  FILE_SHARE_READ, NULL, OPEN_EXISTING,
			  FILE_ATTRIBUTE_NORMAL, NULL );
	if ( fh == INVALID_HANDLE_VALUE ) {
		fprintf ( stderr, "%s: failed CreateFile() with error %#x\n",
			  filename, GetLastError() );
		goto err_create_file;
	}

	/* Enumerate certificates */
	if ( ! ImageEnumerateCertificates ( fh, CERT_SECTION_TYPE_ANY,
					    &num_certs, NULL, 0 ) ) {
		fprintf ( stderr, "%s: failed ImageEnumerateCertificates() "
			  "with error %#x\n", filename, GetLastError() );
		goto err_enumerate;
	}

	/* Add certificate */
	if ( ! ImageAddCertificate ( fh, cert, &index ) ) {
		fprintf ( stderr, "%s: failed ImageAddCertificate() with "
			  "error %#x\n", filename, GetLastError() );
		goto err_add_certificate;
	}

	/* Remove certificate */
	if ( ! ImageRemoveCertificate ( fh, index ) ) {
		fprintf ( stderr, "%s: failed ImageRemoveCertificate() with "
			  "error %#x\n", filename, GetLastError() );
		goto err_remove_certificate;
	}

	/* Free resources */
	CloseHandle ( fh );
	free ( cert );

	return TRUE;

 err_remove_certificate:
 err_add_certificate:
 err_enumerate:
	CloseHandle ( fh );
 err_create_file:
	free ( cert );
 err_alloc:
	return FALSE;
}

static BOOL check_image ( const char *filename ) {
	BOOL ok;

	/* Check MapAndLoad() */
	if ( ! check_map_and_load ( filename ) )
		return FALSE;

	/* Check certificates */
	if ( ! check_certificates ( filename ) )
		return FALSE;

	return TRUE;
}

int main ( int argc, char **argv ) {
	const char *filename;
	BOOL ok;
	DWORD err;
	int i;

	/* Sanity check */
	if ( argc < 2 ) {
		fprintf ( stderr, "Syntax: %s <image> [<image>...]\n",
			  argv[0] );
		exit ( 2 );
	}

	/* Check each image */
	for ( i = 1 ; i < argc ; i++ ) {
		filename = argv[i];
		ok = check_image ( filename );
		if ( ! ok )
			exit ( 1 );
		printf ( "%s: ok\n", filename );
	}

	return 0;
}
